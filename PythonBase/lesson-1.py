num = 15
# id() - выдает номер ячейки в памяти, где хранится переменная
print(id(num))

# Целое число / integer / int
num = 20
print(type(num))

# Дробные числа / вещественные числа / числа с плавающей точкой / float
fl_num = 3.14
print(type(fl_num))

# Строка / string / str
str1 = 'Str1'
str2 = "Str2"
print(type(str1))

# Список / list
lt = [9, 'hello', 7.95]
print(type(lt))

# Кортеж / tuple
tp = (67, 98, 'poka', 0.6)
print(type(tp))

# Множество / set
st = {5, 7, 6, 5, 5, 7, 5, 6, 7, 'hello'}
print(type(st))

# Словарь / dictionary / dict
client = {'name': 'Boris', 'height': 170, 'age': 27}
print(type(client))

# Логический тип / Булевый тип / boolean / bool
logic = True
print(type(logic))
